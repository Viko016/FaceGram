function sumarLike(boton) {
    var contadorLike = boton.parentElement.querySelector('.contadorlike');
    var contadorDislike = boton.parentElement.querySelector('.contadordislike');
    
    if (parseInt(contadorLike.textContent) === 0) 
    {
        contadorLike.textContent = parseInt(contadorLike.textContent) + 1;
        if (parseInt(contadorDislike.textContent) === 1) 
        {
            contadorDislike.textContent = parseInt(contadorDislike.textContent) - 1;
        }
    } 
    else 
    {
        contadorLike.textContent = parseInt(contadorLike.textContent) - 1;
    }
}

